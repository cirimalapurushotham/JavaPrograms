
public class FindIndex {
	
		public static void main(String[] args) {
			
	           String[] s={"blue", "orange","green","pink","block"};
	           
	           int ind=0;
	           for(int i=0;i<s.length;i++)
	           {
	        	   if(s[i]=="green")
	        	   {
	        		   ind=i;
	        	   }
	           }
	           System.out.println("index-->"+ind);
	           
	           
	           
	           
		}

	}

